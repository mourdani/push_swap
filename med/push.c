#include "header.h"

void	push(t_stack *a, t_stack *b)
{
	b->max_i += 1;
	b->stack[b->max_i] = a->stack[a->max_i];
	a->stack[a->max_i] = 0;
	a->max_i -= 1;
	printf("push\n");
}
